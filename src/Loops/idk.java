package Loops;


public class idk {
    public static void main(String[] args) {
        printSquareNumber(526);
    }

    public static void printSquareNumber(int x){



        for( int i=x; i>0; i--){

            for(int j=x;j>0; j--) {
                System.out.print(j + " ");

            }
            System.out.println();
        }

    }
}
